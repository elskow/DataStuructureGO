package main

import (
	"bufio"
	"fmt"
	"os"
)


func main() {
	// Membaca input dari standar masukan
	scanner := bufio.NewScanner(os.Stdin)

	// Loop selama masih ada input
	for scanner.Scan() {
		// Cetak input
		fmt.Println(scanner.Text())
	}
}